# Арифметические операции
# +, -, *, /, //, **
# **, +, -, *, /, //, %,
# (),

# / - деление по умолчанию работает как для вещественных чисел float
# // - деление в целых числах int
# ** - возведение в степень

a = 2
b = 8
c = a ** b
print(c)

# round() - округлению по количеству символов после запятой
a = 3.3
b = 3
c = round(a * b, 3)
print(c)

# присваивание
a += 4
print(a)